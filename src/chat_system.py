import asyncio
import json
import logging
import re
from typing import Dict, Any, Optional, Tuple
from collections import defaultdict
from enum import Enum, auto
from urllib.parse import urlparse

from src.database.memory_manager import MemoryManager
from src.clients.zammad_client import ZammadClient
from config.global_config import SUPPORT_CHANNELS
from src.engine import TextEngine
from src.message_handler import BotLogic
from src.persona import Persona
from src.utils.model_utils import get_model_list
from src.utils.save_utils import save_personas_to_file, load_personas_from_file

logger = logging.getLogger(__name__)


class ResponseType(Enum):
    """Enumeration for the type of response generated by the ChatSystem."""
    DEV_COMMAND = auto()
    LLM_GENERATION = auto()


class ChatSystem:
    def __init__(self, memory_manager: MemoryManager, text_engine: TextEngine, zammad_client: ZammadClient) -> None:
        self.personas: Dict[str, Persona] = load_personas_from_file()
        self.memory_manager: MemoryManager = memory_manager
        self.text_engine: TextEngine = text_engine
        self.zammad_client: ZammadClient = zammad_client
        self.bot_logic: BotLogic = BotLogic(self)
        self.last_api_requests: Dict[str, Dict[str, Optional[Dict[str, Any]]]] = defaultdict(dict)
        self.models_available: Dict[str, Any] = get_model_list()
        self.background_tasks = set()

    def _should_create_ticket(self, channel: str, message: str) -> bool:
        """Determines if an interaction should generate a Zammad ticket."""
        return channel.lower() in [c.lower() for c in SUPPORT_CHANNELS]

    def _find_ticket_id_in_message(self, message: str) -> Optional[int]:
        """Finds a ticket ID pattern like [Ticket#12345] in a message."""
        match = re.search(r'\[Ticket#(\d+)\]', message, re.IGNORECASE)
        if match:
            return int(match.group(1))
        return None

    async def _find_active_ticket_for_user(self, customer_id: int) -> Optional[int]:
        """Finds the most recently updated open or new ticket for a given Zammad user ID."""
        try:
            # A ticket can be in 'new' or 'open' state to be considered active.
            query = f"customer_id:{customer_id} AND state.name:(open OR new)"
            search_results = await asyncio.to_thread(
                self.zammad_client.search_tickets,
                query=query,
                sort_by='updated_at',
                order_by='desc'
            )
            if search_results:
                return search_results[0]['id']
            return None
        except Exception as e:
            logger.error(f"Error searching for active tickets for customer {customer_id}: {e}", exc_info=True)
            return None

    async def _get_or_create_zammad_user(self, user_identifier: str, channel: str,
                                         user_display_name: Optional[str] = None) -> Tuple[
        Optional[int], Optional[str]]:
        """
        Finds a Zammad user by email or creates one. Uses display name for better user creation.
        Returns a tuple of (Zammad user ID, Zammad-registered email).
        """
        email_match = re.search(r'<(.+?)>', user_identifier)
        is_real_email = email_match is not None

        if is_real_email:
            email = email_match.group(1)
            name_part = user_display_name if user_display_name else user_identifier.split('<')[0].strip()
            name_parts = name_part.split()
            firstname = name_parts[0] if name_parts else "Unknown"
            lastname = ' '.join(name_parts[1:]) if len(name_parts) > 1 else "User"
            note = None
        else:
            domain = urlparse(self.zammad_client.api_url).hostname
            email = f"{channel.lower()}-{user_identifier}@{domain}"
            # Use the display name for a more human-readable name in Zammad
            name_parts = user_display_name.split() if user_display_name else [f"{channel.capitalize()} User",
                                                                              user_identifier]
            firstname = name_parts[0]
            lastname = ' '.join(name_parts[1:]) if len(name_parts) > 1 else ""
            note = f"Auto-generated user from {channel.capitalize()}. Original identifier: {user_identifier}"

        try:
            search_results = await asyncio.to_thread(self.zammad_client.search_user, email)
            if search_results:
                return search_results[0]['id'], search_results[0]['email']

            logger.info(f"Creating new Zammad user for identifier '{user_identifier}' with email: {email}")
            new_user = await asyncio.to_thread(
                self.zammad_client.create_user,
                email=email, firstname=firstname, lastname=lastname, note=note
            )
            return new_user['id'], new_user['email']

        except Exception as e:
            logger.error(f"Error getting or creating Zammad user for '{user_identifier}': {e}", exc_info=True)
            return None, None

    async def generate_response(
            self,
            persona_name: str,
            user_identifier: str,
            channel: str,
            message: str,
            image_url: Optional[str] = None,
            history_limit: Optional[int] = None,
            user_display_name: Optional[str] = None
    ) -> Tuple[str, ResponseType, Optional[int]]:
        """
        Orchestrates response generation, returning the response, its type, and any relevant ticket ID.
        NOTE: This method is now LOG-AGNOSTIC. The calling interface is responsible for logging.
        """
        command_result = await self.bot_logic.preprocess_message(persona_name, user_identifier, message)
        if command_result:
            if command_result.get("mutated", False):
                save_personas_to_file(self.personas)
            return command_result["response"], ResponseType.DEV_COMMAND, None

        ticket_to_log = None
        try:
            persona = self.personas.get(persona_name)
            if not persona:
                return "Error: Persona not found.", ResponseType.DEV_COMMAND, None

            # --- CONTEXT GATHERING ---
            is_ticket_channel = self._should_create_ticket(channel, message)
            ticket_id_for_context = None
            customer_id = None
            zammad_email = None

            if is_ticket_channel:
                try:
                    customer_id, zammad_email = await self._get_or_create_zammad_user(user_identifier, channel, user_display_name)
                    if customer_id:
                        ticket_id_for_context = self._find_ticket_id_in_message(message)
                        if not ticket_id_for_context:
                            ticket_id_for_context = await self._find_active_ticket_for_user(customer_id)
                except Exception as zammad_error:
                    logger.error(f"A Zammad API error occurred during user/ticket search: {zammad_error}", exc_info=True)

            effective_limit = persona.get_context_length() if persona.get_context_length() is not None else history_limit
            interaction_history = []
            system_context_message = None
            memory_type = persona.get_memory_type()

            # Priority 1: Ticket History
            if ticket_id_for_context:
                interaction_history = self.memory_manager.get_ticket_history(ticket_id_for_context, effective_limit)
                system_context_message = f"This conversation is part of Zammad ticket #{ticket_id_for_context}. All messages are related to this ticket."
            # Priority 2: Persona Override for "personal" mode
            elif memory_type == "personal":
                interaction_history = self.memory_manager.get_personal_history(user_identifier, persona_name, effective_limit)
            # Priority 3 (Default): Channel History for "auto" and "channel" modes
            else:
                interaction_history = self.memory_manager.get_channel_history(channel, effective_limit)

            if system_context_message:
                interaction_history.insert(0, {"role": "system", "content": system_context_message})

            context_object = {"persona_prompt": persona.get_prompt(), "history": interaction_history,
                              "current_message": {"text": message, "image_url": image_url}}

            # --- ZAMMAD TICKET ACTIONS ---
            if is_ticket_channel and customer_id:
                ticket_to_log = ticket_id_for_context
                try:
                    if ticket_to_log:
                        await asyncio.to_thread(self.zammad_client.add_article_to_ticket, ticket_id=ticket_to_log,
                                                body=message, impersonate_email=zammad_email)
                    else:
                        ticket_title_name = user_display_name if user_display_name else user_identifier.split('<')[0].strip()
                        title = f"New request from {ticket_title_name} via {channel}"
                        new_ticket = await asyncio.to_thread(self.zammad_client.create_ticket, title=title,
                                                               group='Users', customer_id=customer_id)
                        if new_ticket and new_ticket.get('id'):
                            ticket_to_log = new_ticket['id']
                            await asyncio.to_thread(self.zammad_client.add_article_to_ticket,
                                                    ticket_id=ticket_to_log, body=message,
                                                    impersonate_email=zammad_email)
                            logger.info(f"Created new Zammad ticket #{ticket_to_log} and added initial article.")
                        else:
                            logger.error("Zammad client returned a null ticket object after creation attempt.")
                except Exception as zammad_error:
                    logger.error(f"A Zammad API error occurred during ticket action processing: {zammad_error}", exc_info=True)

            # --- LLM GENERATION ---
            reply_content, api_payload = await self.text_engine.generate_response(persona.get_config_for_engine(),
                                                                                  context_object)
            self.last_api_requests[user_identifier][persona_name] = api_payload

            # --- FINAL ZAMMAD UPDATE ---
            if is_ticket_channel and ticket_to_log:
                try:
                    await asyncio.to_thread(self.zammad_client.add_article_to_ticket, ticket_id=ticket_to_log,
                                            body=reply_content)
                except Exception as e:
                    logger.error(f"Failed to add bot reply to Zammad ticket #{ticket_to_log}. Error: {e}", exc_info=True)

            return reply_content, ResponseType.LLM_GENERATION, ticket_to_log

        except Exception as e:
            logger.error(f"A critical error occurred in generate_response for {user_identifier}: {e}", exc_info=True)
            return "An internal error occurred while processing your request.", ResponseType.DEV_COMMAND, ticket_to_log

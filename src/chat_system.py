import asyncio
import json
import logging
import re
from collections import defaultdict
from enum import Enum, auto
from typing import Any, Coroutine, Dict, List, Optional, Set, Tuple
from urllib.parse import urlparse

from config.global_config import SUPPORT_CHANNELS, MAX_TOOL_CALLS
from src.clients.zammad_client import ZammadClient
from src.database.memory_manager import MemoryManager
from src.engine import LLMCommunicationError, TextEngine
from src.message_handler import BotLogic
from src.persona import Persona, ExecutionMode, MemoryMode
from src.tools.tool_manager import ToolManager
from src.utils.model_utils import get_model_list
from src.utils.save_utils import load_personas_from_file, save_personas_to_file

logger = logging.getLogger(__name__)


class ResponseType(Enum):
    """Enumeration for the type of response generated by the ChatSystem."""
    DEV_COMMAND = auto()
    LLM_GENERATION = auto()


class ChatSystem:
    def __init__(self, memory_manager: MemoryManager, text_engine: TextEngine, zammad_client: ZammadClient) -> None:
        self.personas: Dict[str, Persona] = load_personas_from_file() or {}
        self.memory_manager: MemoryManager = memory_manager
        self.text_engine: TextEngine = text_engine
        self.zammad_client: ZammadClient = zammad_client
        self.tool_manager: ToolManager = ToolManager(zammad_client)
        self.bot_logic: BotLogic = BotLogic(self)
        self.last_api_requests: Dict[str, Dict[str, Optional[Dict[str, Any]]]] = defaultdict(dict)
        self.models_available: Dict[str, Any] = get_model_list() or {}
        self.background_tasks: Set[Coroutine[Any, Any, Any]] = set()

    def _should_create_ticket(self, channel: str, message: str) -> bool:
        """Determines if an interaction should generate a Zammad ticket."""
        return channel.lower() in [c.lower() for c in SUPPORT_CHANNELS]

    def _find_ticket_number_in_message(self, message: str) -> Optional[int]:
        """Finds a ticket NUMBER pattern like [Ticket#12345] in a message."""
        match = re.search(r'\[Ticket#(\d+)\]', message, re.IGNORECASE)
        if match:
            return int(match.group(1))
        return None

    async def _get_ticket_id_from_number(self, ticket_number: int) -> Optional[int]:
        """Translates a user-facing ticket number to an internal Zammad ticket ID."""
        try:
            search_results = await asyncio.to_thread(
                self.zammad_client.search_tickets, query=f"number:{ticket_number}"
            )
            if search_results:
                return search_results[0]['id']
            return None
        except Exception as e:
            logger.error(f"Error searching for ticket number {ticket_number}: {e}", exc_info=True)
            return None

    async def _find_active_ticket_for_user(self, customer_id: int) -> Optional[int]:
        """Finds the most recently updated open or new ticket for a given Zammad user ID."""
        try:
            query = f"customer_id:{customer_id} AND state.name:(open OR new)"
            search_results: List[Dict[str, Any]] = await asyncio.to_thread(
                self.zammad_client.search_tickets,
                query=query,
                sort_by='updated_at',
                order_by='desc'
            )
            if search_results:
                return search_results[0]['id']
            return None
        except Exception as e:
            logger.error(f"Error searching for active tickets for customer {customer_id}: {e}", exc_info=True)
            return None

    async def _get_or_create_zammad_user(self, user_identifier: str, channel: str,
                                         user_display_name: Optional[str] = None) -> Tuple[
        Optional[int], Optional[str]]:
        """
        Finds a Zammad user by email or creates one. Uses display name for better user creation.
        Returns a tuple of (Zammad user ID, Zammad-registered email).
        """
        email_match: Optional[re.Match[str]] = re.search(r'<(.+?)>', user_identifier)
        is_real_email: bool = email_match is not None

        email: str
        firstname: str
        lastname: str
        note: Optional[str]

        if is_real_email:
            email = email_match.group(1)
            name_part: str = user_display_name if user_display_name else user_identifier.split('<')[0].strip()
            name_parts: List[str] = name_part.split()
            firstname = name_parts[0] if name_parts else "Unknown"
            lastname = ' '.join(name_parts[1:]) if len(name_parts) > 1 else "User"
            note = None
        else:
            domain: str = urlparse(self.zammad_client.api_url).hostname or "local.host"
            email = f"{channel.lower()}-{user_identifier}@{domain}"
            name_parts = user_display_name.split() if user_display_name else [f"{channel.capitalize()} User",
                                                                              user_identifier]
            firstname = name_parts[0]
            lastname = ' '.join(name_parts[1:]) if len(name_parts) > 1 else ""
            note = f"Auto-generated user from {channel.capitalize()}. Original identifier: {user_identifier}"

        try:
            search_results = await asyncio.to_thread(self.zammad_client.search_user, email)
            if search_results:
                return search_results[0]['id'], search_results[0]['email']

            logger.info(f"Creating new Zammad user for identifier '{user_identifier}' with email: {email}")
            new_user = await asyncio.to_thread(
                self.zammad_client.create_user,
                email=email, firstname=firstname, lastname=lastname, note=note
            )
            return new_user['id'], new_user['email']

        except Exception as e:
            logger.error(f"Error getting or creating Zammad user for '{user_identifier}': {e}", exc_info=True)
            return None, None

    def _format_raw_history_for_llm(self, raw_history: List[Dict[str, Any]], memory_mode: str,
                                    persona_name: str, server_id: Optional[str]) -> List[Dict[str, Any]]:
        """Formats database history records into a list of messages for the LLM."""
        final_history: List[Dict[str, Any]] = []
        is_group_chat = memory_mode in ("server", "global", "ticket") or (
                    memory_mode == "channel" and server_id is not None)

        for msg in raw_history:
            author_role = msg.get('author_role')
            author_name = msg.get('author_name')
            content = msg.get('content', '')

            if author_role == 'user':
                if is_group_chat and author_name:
                    formatted_content = f"{author_name}: {content}"
                    final_history.append({'role': 'user', 'content': formatted_content})
                else:
                    final_history.append({'role': 'user', 'content': content})
            elif author_role == 'assistant':
                if author_name == persona_name:
                    final_history.append({'role': 'assistant', 'content': content})
                else:
                    # In a group chat, messages from other personas.json.json are treated as user messages
                    formatted_content = f"{author_name}: {content}"
                    final_history.append({'role': 'user', 'content': formatted_content})
        return final_history

    async def generate_response(
            self,
            persona_name: str,
            user_identifier: str,
            channel: str,
            message: str,
            server_id: Optional[str] = None,
            image_url: Optional[str] = None,
            history_limit: Optional[int] = None,
            user_display_name: Optional[str] = None
    ) -> Tuple[str, ResponseType, Optional[int]]:
        command_result: Optional[Dict[str, Any]] = await self.bot_logic.preprocess_message(persona_name,
                                                                                           user_identifier, message)
        if command_result:
            if command_result.get("mutated", False):
                save_personas_to_file(self.personas)
            return command_result["response"], ResponseType.DEV_COMMAND, None

        ticket_to_log: Optional[int] = None
        try:
            persona: Optional[Persona] = self.personas.get(persona_name)
            if not persona:
                return "Error: Persona not found.", ResponseType.DEV_COMMAND, None

            is_ticket_channel: bool = self._should_create_ticket(channel, message)
            customer_id: Optional[int] = None
            zammad_email: Optional[str] = None
            mode = persona.get_memory_mode()
            user_facing_ticket_number: Optional[int] = None

            if is_ticket_channel:
                customer_id, zammad_email = await self._get_or_create_zammad_user(user_identifier, channel,
                                                                                  user_display_name)
                if customer_id:
                    user_facing_ticket_number = self._find_ticket_number_in_message(message)
                    if user_facing_ticket_number:
                        ticket_to_log = await self._get_ticket_id_from_number(user_facing_ticket_number)
                        if not ticket_to_log:
                            logger.warning(
                                f"User mentioned ticket number {user_facing_ticket_number}, but it was not found.")
                    else:
                        ticket_to_log = await self._find_active_ticket_for_user(customer_id)

            effective_limit: int = persona.get_context_length()
            if history_limit is not None:
                effective_limit = min(effective_limit, history_limit)

            raw_history: List[Dict[str, Any]] = []
            memory_mode_used = "none"
            system_context_message: Optional[str] = None

            if mode == MemoryMode.TICKET_ISOLATED:
                memory_mode_used = "ticket"
                if ticket_to_log:
                    raw_history = self.memory_manager.get_ticket_history(ticket_to_log, effective_limit)
            elif mode == MemoryMode.SERVER_WIDE:
                if server_id:
                    memory_mode_used = "server"
                    raw_history = self.memory_manager.get_server_history(server_id, persona_name, effective_limit)
                else:
                    memory_mode_used = "server (unavailable)"
            elif mode == MemoryMode.PERSONAL:
                memory_mode_used = "personal"
                raw_history = self.memory_manager.get_personal_history(user_identifier, persona_name, effective_limit)
            elif mode == MemoryMode.GLOBAL:
                memory_mode_used = "global"
                raw_history = self.memory_manager.get_global_history(persona_name, effective_limit)
            elif mode == MemoryMode.CHANNEL_ISOLATED:
                memory_mode_used = "channel"
                raw_history = self.memory_manager.get_channel_history(channel, persona_name, server_id, effective_limit)

            if memory_mode_used == "ticket" and ticket_to_log:
                display_number = user_facing_ticket_number or ticket_to_log
                system_context_message = f"This conversation is part of Zammad ticket #{display_number}."

            conversation_history: List[Dict[str, Any]] = self._format_raw_history_for_llm(raw_history, memory_mode_used,
                                                                                          persona_name, server_id)
            if system_context_message:
                conversation_history.insert(0, {"role": "system", "content": system_context_message})

            all_tools = self.tool_manager.get_tool_definitions()
            enabled_tool_names = persona.get_enabled_tools()
            tools_for_llm: List[Dict[str, Any]] = []
            if enabled_tool_names == ['*']:
                tools_for_llm = all_tools
            elif enabled_tool_names:
                tools_for_llm = [tool for tool in all_tools if
                                 tool.get('function', {}).get('name') in enabled_tool_names]

            if is_ticket_channel and ticket_to_log:
                await asyncio.to_thread(self.zammad_client.add_article_to_ticket, ticket_id=ticket_to_log,
                                        body=message, impersonate_email=zammad_email)

            conversation_history.append({"role": "user", "content": message})

            for i in range(MAX_TOOL_CALLS):
                context_object: Dict[str, Any] = {
                    "persona_prompt": persona.get_prompt(),
                    "history": conversation_history,
                    "current_message": {"text": "", "image_url": image_url if i == 0 else None}
                }
                llm_response, api_payload = await self.text_engine.generate_response(
                    persona.get_config_for_engine(), context_object, tools=tools_for_llm
                )
                if api_payload: self.last_api_requests[user_identifier][persona_name] = api_payload

                if llm_response.get("type") == "text":
                    final_text = llm_response.get("content", "")
                    if is_ticket_channel and ticket_to_log:
                        await asyncio.to_thread(self.zammad_client.add_article_to_ticket, ticket_id=ticket_to_log,
                                                body=final_text)
                    return final_text, ResponseType.LLM_GENERATION, ticket_to_log

                elif llm_response.get("type") == "tool_calls":
                    calls = llm_response.get("calls", [])
                    conversation_history.append({"role": "assistant", "tool_calls": calls})

                    for call in calls:
                        tool_name = call.get("name")
                        tool_args = call.get("arguments", {})
                        tool_result: Dict[str, Any]

                        if persona.get_execution_mode() == ExecutionMode.SILENT_ANALYSIS:
                            log_body = (
                                f"SILENT ANALYSIS: Persona '{persona_name}' intended to call tool '{tool_name}' "
                                f"with arguments: {json.dumps(tool_args)}")
                            logger.info(log_body)
                            if ticket_to_log:
                                await asyncio.to_thread(self.zammad_client.add_article_to_ticket,
                                                        ticket_id=ticket_to_log, body=log_body, internal=True)
                            tool_result = {"status": "success", "action": "simulated and logged"}
                        else:
                            if tool_name == 'create_ticket':
                                if 'customer_id' not in tool_args and customer_id:
                                    tool_args['customer_id'] = customer_id

                            tool_result = await self.tool_manager.execute_tool(tool_name, **tool_args)

                            if tool_name == 'create_ticket' and tool_result.get('result', {}).get('id'):
                                ticket_to_log = tool_result['result']['id']

                        conversation_history.append({
                            "role": "tool",
                            "tool_call_id": call.get("id"),
                            "name": tool_name,
                            "content": json.dumps(tool_result)
                        })
                else:
                    raise LLMCommunicationError("LLM returned an invalid response structure.")

            logger.error(f"Exceeded max tool calls ({MAX_TOOL_CALLS}) for user {user_identifier}.")
            return "I seem to be stuck in a loop. Could you please clarify your request?", ResponseType.DEV_COMMAND, ticket_to_log

        except LLMCommunicationError as e:
            logger.error(f"A recoverable LLM communication error occurred for {user_identifier}: {e}")
            if e.api_payload:
                self.last_api_requests[user_identifier][persona_name] = e.api_payload
            error_msg = ("I'm not sure how to continue. Could you please rephrase?" if "empty response" in str(e) else
                         "Error while generating a response: " + str(e))
            return error_msg, ResponseType.DEV_COMMAND, ticket_to_log
        except Exception as e:
            logger.error(f"A critical error occurred in generate_response for {user_identifier}: {e}", exc_info=True)
            return "An internal error occurred while processing your request.", ResponseType.DEV_COMMAND, ticket_to_log
